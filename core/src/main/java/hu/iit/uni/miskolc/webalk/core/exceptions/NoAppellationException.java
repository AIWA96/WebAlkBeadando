package hu.iit.uni.miskolc.webalk.core.exceptions;

public class NoAppellationException extends Exception {
    public NoAppellationException() {
    }

    public NoAppellationException(String s) {
    }

    public NoAppellationException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public NoAppellationException(Throwable throwable) {
        super(throwable);
    }

    public NoAppellationException(String s, Throwable throwable, boolean b, boolean b1) {
        super(s, throwable, b, b1);
    }
}
