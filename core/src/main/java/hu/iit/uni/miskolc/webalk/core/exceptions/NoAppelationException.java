package hu.iit.uni.miskolc.webalk.core.exceptions;

public class NoAppelationException extends Exception {
    public NoAppelationException() {
    }

    public NoAppelationException(String s) {
    }

    public NoAppelationException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public NoAppelationException(Throwable throwable) {
        super(throwable);
    }

    public NoAppelationException(String s, Throwable throwable, boolean b, boolean b1) {
        super(s, throwable, b, b1);
    }
}
