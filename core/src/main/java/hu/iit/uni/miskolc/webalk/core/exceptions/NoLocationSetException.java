package hu.iit.uni.miskolc.webalk.core.exceptions;

public class NoLocationSetException extends Throwable {
    public NoLocationSetException() {
    }

    public NoLocationSetException(String s) {
    }

    public NoLocationSetException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoLocationSetException(Throwable cause) {
        super(cause);
    }

    public NoLocationSetException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
